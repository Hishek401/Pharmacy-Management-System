    public void loadMedTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        // Clear existing rows
        model.setRowCount(0);

        // Get all medicines from controller
        LinkedList<Medicine> medicineList = controller.getAllMedicines();

        // Loop through medicines and add to table
        for (Medicine m : medicineList) {
            model.addRow(new Object[]{
                m.getMedicineId(),
                m.getMedicineName(),
                m.getManufacturer(),
                m.getPrice(),
                m.getQuantity(),
                m.getExpiryDate()
            });
        }
    }

