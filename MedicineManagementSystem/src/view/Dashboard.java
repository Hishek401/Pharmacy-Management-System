    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // Read values from text fields
            int id = Integer.parseInt(txtId1.getText().trim());
            String manufacturer = txtManufacturer1.getText().trim();
            double price = Double.parseDouble(txtPrice1.getText().trim());
            int quantity = Integer.parseInt(txtQuantity1.getText().trim());

            // Find existing medicine
            Medicine existingMedicine = controller.findById(id);

            if (existingMedicine == null) {
                JOptionPane.showMessageDialog(
                        this,
                        "No medicine found with ID: " + id,
                        "Update Failed",
                        JOptionPane.ERROR_MESSAGE
                );
                return;
            }

            // Remove old instance
            controller.removeById(id);

            // Create updated medicine object
            Medicine updatedMedicine = new Medicine(
                    id,
                    existingMedicine.getMedicineName(),
                    manufacturer,
                    price,
                    quantity,
                    existingMedicine.getExpiryDate()
            );

            // Add updated item
            controller.addItem(updatedMedicine);

            // Reload table
            loadMedTable();

            JOptionPane.showMessageDialog(
                    this,
                    "Medicine updated successfully!",
                    "Success",
                    JOptionPane.INFORMATION_MESSAGE
            );

            // Clear fields (optional)
            txtId.setText("");
            txtManufacturer.setText("");
            txtPrice.setText("");
            txtQuantity.setText("");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(
                    this,
                    "Please enter valid numeric values for ID, Price, and Quantity.",
                    "Input Error",
                    JOptionPane.WARNING_MESSAGE
            );
        }
    }//GEN-LAST:event_jButton2ActionPerformed

