    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // Get values from text fields
            String idText = txtId.getText().trim();
            String name = txtName.getText().trim();
            String manufacturer = txtManufacturer.getText().trim();
            String priceText = txtPrice.getText().trim();
            String quantityText = txtQuantity.getText().trim();
            String expiry = txtExpiry.getText().trim();

            // Validations
            if (idText.isEmpty() || name.isEmpty() || manufacturer.isEmpty()
                    || priceText.isEmpty() || quantityText.isEmpty() || expiry.isEmpty()) {

                JOptionPane.showMessageDialog(
                        null,
                        "All fields are required!",
                        "Validation Error",
                        JOptionPane.ERROR_MESSAGE
                );
                return;
            }

            int id;
            double price;
            int quantity;

            try {
                id = Integer.parseInt(idText);
                price = Double.parseDouble(priceText);
                quantity = Integer.parseInt(quantityText);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(
                        null,
                        "ID, Price, and Quantity must be numeric!",
                        "Invalid Input",
                        JOptionPane.ERROR_MESSAGE
                );
                return;
            }

            if (id <= 0 || price <= 0 || quantity <= 0) {
                JOptionPane.showMessageDialog(
                        null,
                        "ID, Price, and Quantity must be greater than zero!",
                        "Validation Error",
                        JOptionPane.ERROR_MESSAGE
                );
                return;
            }

            // Create Medicine model
            Medicine medicine = new Medicine(
                    id,
                    name,
                    manufacturer,
                    price,
                    quantity,
                    expiry
            );

            // Call controller
            controller.addItem(medicine);

            // Success message
            JOptionPane.showMessageDialog(
                    null,
                    "Medicine added successfully!",
                    "Success",
                    JOptionPane.INFORMATION_MESSAGE
            );

            // Optional: clear fields
            txtId.setText("");
            txtName.setText("");
            txtManufacturer.setText("");
            txtPrice.setText("");
            txtQuantity.setText("");
            txtExpiry.setText("");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(
                    null,
                    "Error adding medicine: " + ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
        }

    }//GEN-LAST:event_jButton1ActionPerformed

